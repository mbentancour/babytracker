/**
 * Calculate BMI from weight (kg) and height (cm).
 * Returns null if either value is missing.
 */
export function calculateBMI(weightKg, heightCm) {
  if (!weightKg || !heightCm || heightCm === 0) return null;
  const heightM = heightCm / 100;
  return weightKg / (heightM * heightM);
}

/**
 * Get the baby's age in months from birth date.
 */
export function getAgeInMonths(birthDate) {
  if (!birthDate) return null;
  const birth = new Date(birthDate);
  const now = new Date();
  return (now.getFullYear() - birth.getFullYear()) * 12 + (now.getMonth() - birth.getMonth());
}

/**
 * Interpolate a percentile value at a given age from a WHO data array.
 */
export function interpolatePercentile(data, ageMonths, percentile) {
  const key = `p${percentile}`;
  // Find surrounding data points
  let lower = data[0];
  let upper = data[data.length - 1];

  for (let i = 0; i < data.length - 1; i++) {
    if (data[i].month <= ageMonths && data[i + 1].month >= ageMonths) {
      lower = data[i];
      upper = data[i + 1];
      break;
    }
  }

  if (lower.month === upper.month) return lower[key];

  // Linear interpolation
  const ratio = (ageMonths - lower.month) / (upper.month - lower.month);
  return lower[key] + ratio * (upper[key] - lower[key]);
}
